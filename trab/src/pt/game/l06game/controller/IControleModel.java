public interface IControleModel {
    /**
     * Informa o Controle que um peixe foi capturado.
     */
    public void capturouPeixe();

    /**
     * Informa o Controle que a bola de la foi capturada.
     */ 
    public void capturouBolaDeLa();

    /**
     * Informa o Controle que o cachorro alcancou o gato.
     */
    public void cachorroAlcancou();

    /**
     * Informa o Controle que o gato chegou na sua toca.
     */
    public void chegouNaToca();

}